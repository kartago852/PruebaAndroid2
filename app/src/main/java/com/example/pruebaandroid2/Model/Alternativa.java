package com.example.pruebaandroid2.Model;

public class Alternativa {

    private String IdAlternativa;
    private String NombreAlt;
    private String ValorAlt;

    public Alternativa(){

    }

    public String getIdAlternativa() {
        return IdAlternativa;
    }

    public void setIdAlternativa(String idAlternativa) {
        IdAlternativa = idAlternativa;
    }

    public String getNombreAlt() {
        return NombreAlt;
    }

    public void setNombreAlt(String nombreAlt) {
        NombreAlt = nombreAlt;
    }

    public String getValorAlt() {
        return ValorAlt;
    }

    public void setValorAlt(String valorAlt) {
        ValorAlt = valorAlt;
    }
}
