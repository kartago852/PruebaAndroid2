package com.example.pruebaandroid2.Model;

public class Criterio {

     private String IdCriterio;
     private String NombreCri;
     private String ValorCri;
     public  Criterio()
     {

     }

    public String getIdCriterio() {
        return IdCriterio;
    }

    public void setIdCriterio(String idCriterio) {
        IdCriterio = idCriterio;
    }

    public String getNombreCri() {
        return NombreCri;
    }

    public void setNombreCri(String nombreCri) {
        NombreCri = nombreCri;
    }
    public String getValorCri() {
        return ValorCri;
    }

    public void setValorCri(String valorCri) {
        ValorCri = valorCri;
    }
}
